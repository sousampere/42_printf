#include "libft.h"

void	ft_bzero(void *dst, size_t n)
{
	size_t	i;
	char	*new_dst;

	i = 0;
	new_dst = dst;
	while (i < n)
	{
		new_dst[i] = '\0';
		i++;
	}
}
