#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*c_s;

	i = 0;
	c_s = s;
	while (i < n)
	{
		c_s[i] = (char) c;
		i++;
	}
	return (s);
}
